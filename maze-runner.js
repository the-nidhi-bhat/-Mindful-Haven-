// Load level
function loadLevel(level) {
    maze = LEVELS[level - 1];
    player = { x: 1, y: 1 };
    goal = { x: 18, y: 18 };
    levelNumEl.textContent = level;
}

// Keyboard controls
document.addEventListener('keydown', (e) => {
    if (levelCompleteEl.classList.contains('show') || gameOverEl.classList.contains('show')) return;

    let newX = player.x;
    let newY = player.y;

    switch (e.key) {
        case 'ArrowUp':
            newY--;
            e.preventDefault();
            break;
        case 'ArrowDown':
            newY++;
            e.preventDefault();
            break;
        case 'ArrowLeft':
            newX--;
            e.preventDefault();
            break;
        case 'ArrowRight':
            newX++;
            e.preventDefault();
            break;
        default:
            return;
    }

    // Check collision
    if (newX >= 0 && newX < GRID_SIZE && newY >= 0 && newY < GRID_SIZE && maze[newY][newX] === 0) {
        player.x = newX;
        player.y = newY;

        // Check win
        if (player.x === goal.x && player.y === goal.y) {
            if (currentLevel === 10) {
                showGameOver();
            } else {
                showLevelComplete();
            }
        }
    }
});

// Render
function render() {
    ctx.fillStyle = savedTheme === 'dark' ? '#0f1419' : '#f9fafb';
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    // Draw maze
    for (let y = 0; y < GRID_SIZE; y++) {
        for (let x = 0; x < GRID_SIZE; x++) {
            if (maze[y][x] === 1) {
                ctx.fillStyle = savedTheme === 'dark' ? '#334155' : '#e5e7eb';
                ctx.fillRect(x * CELL_SIZE, y * CELL_SIZE, CELL_SIZE, CELL_SIZE);
                ctx.strokeStyle = savedTheme === 'dark' ? '#1e293b' : '#d1d5db';
                ctx.strokeRect(x * CELL_SIZE, y * CELL_SIZE, CELL_SIZE, CELL_SIZE);
            }
        }
    }

    // Draw goal
    ctx.fillStyle = '#2ecc71';
    ctx.fillRect(goal.x * CELL_SIZE + 2, goal.y * CELL_SIZE + 2, CELL_SIZE - 4, CELL_SIZE - 4);
    ctx.strokeStyle = '#27ae60';
    ctx.lineWidth = 2;
    ctx.strokeRect(goal.x * CELL_SIZE + 2, goal.y * CELL_SIZE + 2, CELL_SIZE - 4, CELL_SIZE - 4);

    // Draw player
    ctx.fillStyle = '#6366f1';
    ctx.beginPath();
    ctx.arc(player.x * CELL_SIZE + CELL_SIZE / 2, player.y * CELL_SIZE + CELL_SIZE / 2, CELL_SIZE / 2 - 4, 0, Math.PI * 2);
    ctx.fill();
    ctx.strokeStyle = '#4f46e5';
    ctx.lineWidth = 2;
    ctx.stroke();

    requestAnimationFrame(render);
}

// Timer
function updateTimer() {
    const elapsed = Math.floor((Date.now() - startTime) / 1000);
    const mins = Math.floor(elapsed / 60);
    const secs = elapsed % 60;
    timerEl.textContent = `${mins}:${secs.toString().padStart(2, '0')}`;
}

function showLevelComplete() {
    levelCompleteEl.classList.add('show');
}

function showGameOver() {
    const elapsed = Math.floor((Date.now() - startTime) / 1000);
    const mins = Math.floor(elapsed / 60);
    const secs = elapsed % 60;
    document.getElementById('finalTime').textContent = `${mins}:${secs.toString().padStart(2, '0')}`;
    gameOverEl.classList.add('show');
}

function nextLevel() {
    levelCompleteEl.classList.remove('show');
    currentLevel++;
    loadLevel(currentLevel);
}

function restartGame() {
    gameOverEl.classList.remove('show');
    currentLevel = 1;
    startTime = Date.now();
    loadLevel(currentLevel);
}

